print("2 — Поехать направо (за богатством)")
print("3 — Поехать налево (за семейным счастьем)")
choice = input("\nВведите номер пути (1, 2 или 3) или направление (прямо, направо, налево): ").strip().lower()
if choice == "1" or choice == "прямо":
    print("\nВитязь твёрдо решает ехать прямо — навстречу опасности.")
    print("Витязь вступает в жестокую битву с полчищем врагов.Благодаря отваге и мастерству витязь побеждает, но получает тяжёлые раны.")
elif choice == "2" or choice == "направо":
    print("\nВитязь выбирает путь направо — в поисках богатства.")
    print("Он находит древний клад, спрятанный в пещере, охраняемой драконом.Храбростью и смекалкой витязь обманывает чудовище и забирает сокровища.")
elif choice == "3" or choice == "налево":
    print("\nВитязь решает поехать налево — в поисках семейного счастья.")
    print("В далёкой деревне он встречает прекрасную девушку, они влюбляются друг в друга и заключают брак, спустя время у них рождаются чудесные детки.")   
else:
    print("\nВитязь долго стоит в раздумьях, не решаясь сделать выбор.Наконец, он понимает, что бездействие — тоже выбор.")
    print("Вернувшись домой, витязь становится советником князя, передавая свой опыт молодому поколению.")
    print("Его жизнь течёт спокойно, но в глубине души он всегда будет гадать: что было бы, выбери он иной путь?")